# Видалення рядків із NA
cor_data <- na.omit(cor_data)

# Розрахунок кореляційної матриці
cor_matrix <- cor(cor_data, use = "complete.obs")

# Побудова кореляційної матриці
corrplot(cor_matrix, 
         method = "color",        
         type = "full",          
         addCoef.col = "black",   
         tl.col = "grey",        
         number.cex = 0.7,        
         col = colorRampPalette(c("purple", "white", "orange"))(200)  
)

