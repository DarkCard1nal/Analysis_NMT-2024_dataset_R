  # оскільки деякі цифри в нас з комою, то замінюємо її на крапку
  x_fixed <- gsub(",", ".", x)
  # тепер можемо пеертворити character спочатку в numeric, а потім в integer
  as.integer(as.numeric(x_fixed))
})

